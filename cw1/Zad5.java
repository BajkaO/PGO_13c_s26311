package cw1;

public class Zad5 {
/*Utwórz program, który wypisuje tabliczkę mnożenia od 1 do 10. Na przykład:*/


    public static void main(String[] args) {

        for (int i = 1; i < 11; i++) {
            for (int j = 1; j < 11; j++) {
                System.out.print(i*j + " ");
            }
            System.out.println();
        }

    }



}
